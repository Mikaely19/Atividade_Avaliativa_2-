public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Tabela de Multiplicação");
        
        Int numero = 5 ;
        for ( int i = 1; i <= 10; i ++){
            int resultado = numero * i;
            System.out.println("5  * " + i + " = " + resultado);

        }

    }
}
